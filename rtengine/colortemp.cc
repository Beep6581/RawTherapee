ColorTemp::ColorTemp (double t, double g, double e, const Glib::ustring &m) : temp(t), green(g), equal(e), method(m)
{

    clip (temp, green, equal);
}

void ColorTemp::clip (double &temp, double &green)
{

    if (temp < MINTEMP) {
        temp = MINTEMP;
    } else if (temp > MAXTEMP) {
        temp = MAXTEMP;
    }

    if (green < MINGREEN) {
        green = MINGREEN;
    } else if (green > MAXGREEN) {
        green = MAXGREEN;
    }
}

void ColorTemp::clip (double &temp, double &green, double &equal)
{

    if (temp < MINTEMP) {
        temp = MINTEMP;
    } else if (temp > MAXTEMP) {
        temp = MAXTEMP;
    }

    if (green < MINGREEN) {
        green = MINGREEN;
    } else if (green > MAXGREEN) {
        green = MAXGREEN;
    }

    if(equal < MINEQUAL) {
        equal = MINEQUAL;
    } else if(equal > MAXEQUAL) {
        equal = MAXEQUAL;
    }
}

ColorTemp::ColorTemp (double mulr, double mulg, double mulb, double e) : equal(e)
{
    method = "Custom";
    mul2temp (mulr, mulg, mulb, equal, temp, green);
}

void ColorTemp::mul2temp (const double rmul, const double gmul, const double bmul, const double equal, double& temp, double& green) const
{

    double maxtemp = double(MAXTEMP), mintemp = double(MINTEMP);
    double tmpr, tmpg, tmpb;
    temp = (maxtemp + mintemp) / 2;

    while (maxtemp - mintemp > 1) {
        temp2mul (temp, 1.0, equal, tmpr, tmpg, tmpb);

        if (tmpb / tmpr > bmul / rmul) {
            maxtemp = temp;
        } else {
            mintemp = temp;
        }

        temp = (maxtemp + mintemp) / 2;
    }

    green = (tmpg / tmpr) / (gmul / rmul);
    clip (temp, green);
}


